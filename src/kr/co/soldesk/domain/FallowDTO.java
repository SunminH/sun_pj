package kr.co.soldesk.domain;

public class FallowDTO {
	private int unum_t;
	private int unum_s;
	
	public FallowDTO() {
		super();
	}
	public FallowDTO(int unum_t, int unum_s) {
		super();
		this.unum_t = unum_t;
		this.unum_s = unum_s;
	}
	public int getUnum_t() {
		return unum_t;
	}
	public void setUnum_t(int unum_t) {
		this.unum_t = unum_t;
	}
	public int getUnum_s() {
		return unum_s;
	}
	public void setUnum_s(int unum_s) {
		this.unum_s = unum_s;
	}
	
	
	
	
	
	
}
